# OPERADORES DE ATRIBUIÇAO 
# Simplificar o código e evitar a repetição de variáveis
print('---' * 10);
print('OPERADORES DE ATRIBUIÇAO');
print('---' * 10);

# = Atribuição simples
print('Atribuição simples');
numero = 10;
print(numero)

# +=
print('Atribuição com adição');
x = 10;
x += 5;  # Equivalente a x = x + 5
print(x);

# -=
print('Atribuição com subtração');
a = 10;
a -= 3;  # Equivalente a x = x - 3
print(a); 

# *=
print('Atribuição com multiplicação');
b = 4;
b *= 3;  # Equivalente a x = x * 3
print(b);

# /=
print('Atribuição com divisão');
c = 20;
c /= 4;  # Equivalente a x = x / 4
print(c);  

# //=
print('Atribuição divisão inteira');
d = 20
d //= 3  # Equivalente a x = x // 3
print(d)  # Saída: 6

# %=
print('Atribuição com módulo');
e = 20;
e //= 3;  # Equivalente a x = x // 3
print(e);  

# **=
print('Atribuição com exponenciação');
f = 2
f **= 3  # Equivalente a x = x ** 3
print(f)  