# Tipos de dados
print('--' * 10);
print('TIPOS DE DADOS');
print('--' * 10);

# Inteiro
n1: int = 5
n2: int = -3
print('TIPO INTEIRO: {}, {}'.format(n1, n2))

# String
frase: str = 'Seja bem vindo ao Risort Perola Negra'
num_text: str = '100'
text: str = 'Hello World!'
print('TIPO STRING: {}, {}, {}'.format(frase, num_text, text))

# Booleano
valor: bool = True
result: bool = False
print('TIPO BOOLEANO: {}, {}'.format(valor, result))

# Float
altura: float = 1.80
pi: float = 3.14
print('TIPO FLOAT: {}, {}'.format(altura, pi))

